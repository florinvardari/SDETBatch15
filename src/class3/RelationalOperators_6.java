package class3;

public class RelationalOperators_6 {
    public static void main(String[] args) {
        /*
        Arithmetic operators
        int+int=int
        int/int=int
        int-int=int
         */

        /*
        relation operators
        >
        +>
        ==
        /=
        <
        =<
         */

        int number=10;
        int number2=10;
        System.out.println(number+number2);
        boolean result=20>10;
        System.out.println(result);
        System.out.println(20>10);
        System.out.println(5>10);
        System.out.println(5!=10);//is 5 not equal to 10
        System.out.println(5==10);// is 5 equal to 10  "=="
        System.out.println(5==5);
        System.out.println(10>=20);// is 10 greater or equal to 20
        System.out.println(10+25+38>20);
        System.out.println(10<=10);


    }
}
